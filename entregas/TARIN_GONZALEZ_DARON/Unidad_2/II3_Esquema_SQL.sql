-- Star schema for International Migration (Citizenship by Visa by Country of Last Permanent Residence)
DROP TABLE IF EXISTS fact_migration;
DROP TABLE IF EXISTS dim_time;
DROP TABLE IF EXISTS dim_visa_type;
DROP TABLE IF EXISTS dim_last_residence_country;
DROP TABLE IF EXISTS dim_citizenship_country;

CREATE TABLE dim_citizenship_country (
  citizenship_country_id INT PRIMARY KEY,
  citizenship_country_name VARCHAR(200) NOT NULL UNIQUE
);

CREATE TABLE dim_last_residence_country (
  last_residence_country_id INT PRIMARY KEY,
  last_residence_country_name VARCHAR(200) NOT NULL UNIQUE
);

CREATE TABLE dim_visa_type (
  visa_type_id INT PRIMARY KEY,
  visa_type_name VARCHAR(200) NOT NULL UNIQUE
);

CREATE TABLE dim_time (
  time_id INT PRIMARY KEY,
  date DATE NOT NULL,
  year INT NOT NULL,
  month INT NOT NULL,
  month_name VARCHAR(20) NOT NULL
);

CREATE TABLE fact_migration (
  fact_id BIGSERIAL PRIMARY KEY,
  citizenship_country_id INT NOT NULL REFERENCES dim_citizenship_country(citizenship_country_id),
  last_residence_country_id INT NOT NULL REFERENCES dim_last_residence_country(last_residence_country_id),
  visa_type_id INT NOT NULL REFERENCES dim_visa_type(visa_type_id),
  time_id INT NOT NULL REFERENCES dim_time(time_id),
  migrants_count NUMERIC(18,2) NOT NULL,
  row_hash VARCHAR(64)
);

